public class Point {
	public int x;
	public int y;

	public Point(final int x, final int y) {// final
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public void move(int x, int y) {
		getX();
		getY();
	}
}
